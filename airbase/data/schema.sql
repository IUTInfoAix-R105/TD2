-- Airbase étendu - Schéma adapté pour PostgreSQL/SQLite (R1.05 TD2, exercice 1)
-- Airbase (PILOTE, AVION, VOL) + relations FORMATION et STAGE.
-- Nommage en MAJUSCULES (NUMPIL, VILLE_DEP, TYPE_F...) conforme au corrigé du TD2.

DROP TABLE IF EXISTS FORMATION;
DROP TABLE IF EXISTS STAGE;
DROP TABLE IF EXISTS VOL;
DROP TABLE IF EXISTS AVION;
DROP TABLE IF EXISTS PILOTE;

CREATE TABLE PILOTE (
    NUMPIL   INTEGER,
    NOMPIL   VARCHAR(20),
    ADR      VARCHAR(20),
    SALAIRE  NUMERIC(8,2),
    CONSTRAINT pk_PILOTE PRIMARY KEY (NUMPIL)
);

CREATE TABLE AVION (
    NUMAV        INTEGER,
    NOMAV        VARCHAR(20),
    CAPACITE     INTEGER,
    LOCALISATION VARCHAR(20),
    CONSTRAINT pk_AVION PRIMARY KEY (NUMAV)
);

CREATE TABLE VOL (
    NUMVOL     INTEGER,
    NUMPIL     INTEGER,
    NUMAV      INTEGER,
    VILLE_DEP  VARCHAR(20),
    VILLE_ARR  VARCHAR(20),
    CONSTRAINT pk_VOL PRIMARY KEY (NUMVOL),
    CONSTRAINT fk_VOL_PILOTE FOREIGN KEY (NUMPIL) REFERENCES PILOTE(NUMPIL),
    CONSTRAINT fk_VOL_AVION  FOREIGN KEY (NUMAV)  REFERENCES AVION(NUMAV)
);

CREATE TABLE STAGE (
    TYPE_F     VARCHAR(10),
    NOMAV      VARCHAR(20),
    CATEGORIE  VARCHAR(20),
    CONSTRAINT pk_STAGE PRIMARY KEY (TYPE_F)
);

CREATE TABLE FORMATION (
    NUMPIL       INTEGER,
    DATE         VARCHAR(10),
    APPRECIATION VARCHAR(20),
    TYPE_F       VARCHAR(10),
    CONSTRAINT pk_FORMATION PRIMARY KEY (NUMPIL, DATE),
    CONSTRAINT fk_FORMATION_PILOTE FOREIGN KEY (NUMPIL) REFERENCES PILOTE(NUMPIL),
    CONSTRAINT fk_FORMATION_STAGE  FOREIGN KEY (TYPE_F) REFERENCES STAGE(TYPE_F)
);
