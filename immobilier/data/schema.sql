-- Agence immobilière - Schéma adapté pour PostgreSQL/SQLite (R1.05 TD2, exercice 2)
-- Gestion de propriétaires, locataires et appartements.

DROP TABLE IF EXISTS LOCATAIRE;
DROP TABLE IF EXISTS APPART;
DROP TABLE IF EXISTS PROPRIO;

CREATE TABLE PROPRIO (
    CODEP   INTEGER,
    NOM     VARCHAR(20),
    PRENOM  VARCHAR(20),
    CONSTRAINT pk_PROPRIO PRIMARY KEY (CODEP)
);

CREATE TABLE APPART (
    NUM      INTEGER,
    TYPE     VARCHAR(10),
    ADR      VARCHAR(30),
    VILLE    VARCHAR(20),
    SURFACE  INTEGER,
    LOYER    NUMERIC(8,2),
    CODE     INTEGER,
    CONSTRAINT pk_APPART PRIMARY KEY (NUM),
    CONSTRAINT fk_APPART_PROPRIO FOREIGN KEY (CODE) REFERENCES PROPRIO(CODEP)
);

CREATE TABLE LOCATAIRE (
    CODEL    INTEGER,
    NOM      VARCHAR(20),
    PRENOM   VARCHAR(20),
    NUM      INTEGER,
    CONSTRAINT pk_LOCATAIRE PRIMARY KEY (CODEL),
    CONSTRAINT fk_LOCATAIRE_APPART FOREIGN KEY (NUM) REFERENCES APPART(NUM)
);
